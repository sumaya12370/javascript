// Assignment Operators in JavaScript

//  = (Assignment)
let x = 10;
console.log(x); // 10

//  += (Addition Assignment)
x += 5; // x = x + 5- this is the long way of writing it 
console.log(x); // 15

// 3. -= (Subtraction Assignment)
x -= 3; // x = x - 3
console.log(x); // 12

//  *= (Multiplication Assignment)
x *= 2; // x = x * 2
console.log(x); // 24

//  /= (Division Assignment)
x /= 4; // x = x / 4
console.log(x); // 6

//  %= (Modulus Assignment)
x %= 4; // x = x % 4
console.log(x); // 2

// **= (Exponentiation Assignment)
x **= 3; // x = x ** 3 (2^3)
console.log(x); // 8
