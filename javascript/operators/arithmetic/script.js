var a = 10;
var b = 3;

console.log("a + b = " + (a + b));         // Addition
console.log("a - b = " + (a - b));         // Subtraction
console.log("a * b = " + (a * b));         // Multiplication
console.log("a / b = " + (a / b));         // Division
console.log("a % b = " + (a % b));         // Modulus (remainder)
console.log("a ** b = " + (a ** b));       // Exponentiation

var c = a + b;
console.log("c = a + b = " + c);

var d = c * 2;
console.log("d = c * 2 = " + d);

var e = d - a;
console.log("e = d - a = " + e);

var f = e / b;
console.log("f = e / b = " + f);
